    function getBulan(tanggal_lahir) {
       let sekarang = new Date();
       let selisih = (sekarang-tanggal_lahir);
       let umur = Math.floor(selisih / (365.25 * 24 * 60 * 60 * 1000));
       let bulan = Math.floor(selisih % (365.25 * 24 * 60 * 60 * 1000) / (30 * 24 * 60 * 60 * 1000));
       let total_bulan = (umur * 12) + bulan;
      return total_bulan;
    }

    /* Fungsi formatRupiah */
    function formatRupiah(angka, prefix) {
      var number_string = angka.toString(),
      split = number_string.split(","),
      sisa = split[0].length % 3,
      rupiah = split[0].substr(0, sisa),
      ribuan = split[0].substr(sisa).match(/\d{3}/gi);

      // tambahkan titik jika yang di input sudah menjadi angka ribuan
      if (ribuan) {
        separator = sisa ? "." : "";
        rupiah += separator + ribuan.join(".");
      }

      rupiah = split[1] != undefined ? rupiah + "," + split[1] : rupiah;
      return prefix == undefined ? rupiah : rupiah ? "Rp. " + rupiah : "";
    }

